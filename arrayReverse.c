#include <stdio.h>

void reverse(int *arrayFirstElement, int size)
{
    size--;
    for (int i = 0; size > i; i++)
    {
        int save = *(arrayFirstElement + i);
        *(arrayFirstElement + i) = *(arrayFirstElement + size);
        *(arrayFirstElement + size) = save;
        size--;
    }
}

void print(int *arrayFirstElement, int size)
{
    for (int i = 0; i < size; i++)
    {
        printf("%d\n", *(arrayFirstElement + i));
    }
}

void main()
{
    int arr[] = {1,2,3,4,5,6,7,8,9};
    int size = sizeof(arr) / sizeof(int);
    reverse(arr, size);
    print(arr, size);
}







































































































































































































































































































































































































































